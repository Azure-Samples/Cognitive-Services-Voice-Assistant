namespace UWPVoiceAssistantSample
{
    /// <summary>
    /// Collection of Unicode point definitions used by the Segoe UI MDL2 font symbol
    /// assets.
    /// https://docs.microsoft.com/windows/uwp/design/style/segoe-ui-symbol-font.
    /// </summary>
    public static class Glyphs
    {
        /// <summary>
        /// Gets the Unicode point for a Cancel icon, an 'X' symbol.
        /// </summary>
        public static readonly string Cancel = "\uE711";

        /// <summary>
        /// Gets the Unicode point for a Microphone icon.
        /// </summary>
        public static readonly string Microphone = "\uE720";

        /// <summary>
        /// Gets the Unicode point for a Warning icon, an exclamation in a triangle.
        /// </summary>
        public static readonly string Warning = "\uE7BA";

        /// <summary>
        /// Gets the Unicode point for a battery charging icon, mostly depleted.
        /// </summary>
        public static readonly string BatteryCharging3 = "\uE85D";

        /// <summary>
        /// Gets the Unicode point for an icon of a person with a chat bubble.
        /// </summary>
        public static readonly string FeedbackApp = "\uE939";

        /// <summary>
        /// Gets the Unicode point for an edit icon.
        /// </summary>
        public static readonly string Edit = "\uE70F";

        /// <summary>
        /// Gets the Unicode point for an upload icon.
        /// </summary>
        public static readonly string Load = "\uE898";

        public static readonly string MicrophoneListening = "\uF12E";

        public static readonly string MicrophoneOff = "\uEC54";
    }
}
